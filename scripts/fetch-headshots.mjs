// One-off asset fetcher: downloads every selectable season's driver portraits
// from F1's media CDN into `public/headshots/<season>/<code>.<ext>` and writes a
// typed manifest at `lib/headshots.generated.ts`. Re-run when the season window
// or rosters change:  `node scripts/fetch-headshots.mjs`
//
// Source coverage (verified): legacy portraits exist for 2021–2025 (clean .png),
// the modern Cloudinary cutout for 2024–2026 (.webp). We prefer legacy for
// ≤2025 and modern for 2026.

import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT_DIR = join(ROOT, "public", "headshots");
const MANIFEST = join(ROOT, "lib", "headshots.generated.ts");
const CDN_VERSION = "v1740000001";

// Selectable window: current season + previous 4 (keep in sync with lib/season.ts).
const CURRENT = 2026;
const SEASONS = Array.from({ length: 5 }, (_, i) => String(CURRENT - i));

// Ergast constructorId → F1 media slug (keep in sync with lib/season.ts).
const F1_SLUG = {
  mercedes: "mercedes",
  ferrari: "ferrari",
  mclaren: "mclaren",
  red_bull: "redbullracing",
  alpine: "alpine",
  rb: "racingbulls",
  haas: "haasf1team",
  williams: "williams",
  aston_martin: "astonmartin",
  audi: "audi",
  cadillac: "cadillac",
  sauber: "kicksauber",
};

const norm = (s) =>
  s
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .replace(/[^a-zA-Z]/g, "")
    .toLowerCase();

function driverCode(given, family) {
  const g = norm(given).slice(0, 3);
  const f = norm(family).slice(0, 3);
  if (g.length < 3 || f.length < 3) return null;
  return `${g}${f}01`;
}

function legacyUrl(season, code) {
  return `https://media.formula1.com/content/dam/fom-website/2018-redesign-assets/drivers/${season}/${code}.png`;
}

function modernUrl(season, slug, code) {
  return `https://media.formula1.com/image/upload/c_lfill,w_440/q_auto/${CDN_VERSION}/common/f1/${season}/${slug}/${code}/${season}${slug}${code}right.webp`;
}

async function getDrivers(season) {
  const res = await fetch(
    `https://api.jolpi.ca/ergast/f1/${season}/driverStandings.json`,
    { headers: { Accept: "application/json" } },
  );
  if (!res.ok) throw new Error(`standings ${season}: ${res.status}`);
  const json = await res.json();
  const list =
    json.MRData.StandingsTable.StandingsLists[0]?.DriverStandings ?? [];
  return list.map((d) => ({
    code: driverCode(d.Driver.givenName, d.Driver.familyName),
    name: `${d.Driver.givenName} ${d.Driver.familyName}`,
    slug: F1_SLUG[d.Constructors.at(-1)?.constructorId],
  }));
}

async function download(url) {
  const res = await fetch(url);
  if (!res.ok) return null;
  return Buffer.from(await res.arrayBuffer());
}

async function main() {
  const manifest = {};
  let saved = 0;
  let missed = 0;

  for (const season of SEASONS) {
    const drivers = await getDrivers(season);
    await mkdir(join(OUT_DIR, season), { recursive: true });

    for (const d of drivers) {
      if (!d.code) continue;
      // Prefer legacy (.png, 2021–2025); fall back to modern (.webp, 2024–2026).
      const attempts = [
        { url: legacyUrl(season, d.code), ext: "png" },
        ...(d.slug
          ? [{ url: modernUrl(season, d.slug, d.code), ext: "webp" }]
          : []),
      ];

      let done = false;
      for (const a of attempts) {
        const buf = await download(a.url);
        if (!buf) continue;
        const rel = `headshots/${season}/${d.code}.${a.ext}`;
        await writeFile(join(ROOT, "public", rel), buf);
        manifest[`${season}/${d.code}`] = `/${rel}`;
        saved++;
        done = true;
        break;
      }
      if (!done) {
        missed++;
        console.warn(`  miss: ${season} ${d.name} (${d.code})`);
      }
    }
    console.log(`${season}: ${drivers.length} drivers processed`);
  }

  const entries = Object.keys(manifest)
    .sort()
    .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(manifest[k])},`)
    .join("\n");
  const ts = `// AUTO-GENERATED by scripts/fetch-headshots.mjs — do not edit by hand.
// Maps "<season>/<driverCode>" to a locally stored portrait under /public.
export const LOCAL_HEADSHOTS: Record<string, string> = {
${entries}
};
`;
  await writeFile(MANIFEST, ts);
  console.log(`\nsaved ${saved} portraits, ${missed} missing → ${MANIFEST}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
